  public static FeqOrderexecutionEndPK fromString( String pkValueString ) 
      throws NumberFormatException
  {
    FeqOrderexecutionEndPK pk = new FeqOrderexecutionEndPK();
    int i = pkValueString.indexOf(',');
    pk.institution_code = pkValueString.substring(0,i);
    pk.market_code = pkValueString.substring(i+1);
    pk.m_id = pkValueString;
    return pk;
  }

  /** 
   * コンストラクタ作成時と同順でカンマ区切りの主キー値の文字列を返します。 
   */
  public String toString() {
    if ( m_id == null )
      m_id = institution_code + "," + market_code;
    return m_id;
  }

  private String m_id = null;


